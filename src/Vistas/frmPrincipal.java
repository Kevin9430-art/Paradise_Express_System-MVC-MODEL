    private void btniniciarsesionActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btniniciarsesionActionPerformed
        String rolSeleccionado = cbrol.getSelectedItem().toString();
        String claveIngresada = txtcontrasena.getText();
        cLogin controladorLogin = new cLogin();

        if (controladorLogin.validar(claveIngresada, rolSeleccionado)) {
            JOptionPane.showMessageDialog(this, "Bienvenido " + rolSeleccionado);

            if (rolSeleccionado.equalsIgnoreCase("GERENTE")) {
                panelGerente pg = new panelGerente();
                pg.setPrincipal(this);           // ← le pasamos la referencia al JFrame actual
                mostrarPanel(pg);
            } else if (rolSeleccionado.equalsIgnoreCase("VENDEDOR")) {
                mostrarPanel(new panelVendedor()); // O cualquier otro panel para vendedor
            }
        } else {
            JOptionPane.showMessageDialog(this, "Clave o rol incorrecto", "Error", JOptionPane.ERROR_MESSAGE);
            txtcontrasena.setText("");
        }
    }//GEN-LAST:event_btniniciarsesionActionPerformed

    private void cbrolItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_cbrolItemStateChanged
        if (evt.getStateChange() == java.awt.event.ItemEvent.SELECTED) {
            String rolSeleccionado = cbrol.getSelectedItem().toString();
            if (rolSeleccionado.equalsIgnoreCase("ROL USUARIO")) {
                txtcontrasena.setEnabled(false);
                txtcontrasena.setText("");
            } else {
                txtcontrasena.setEnabled(true);
            }
        }
    }//GEN-LAST:event_cbrolItemStateChanged

