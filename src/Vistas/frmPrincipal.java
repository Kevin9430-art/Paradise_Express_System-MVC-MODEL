    public frmPrincipal() {
        initComponents();

        // Control inicial al abrir el formulario
        if (cbrol.getSelectedItem().toString().equalsIgnoreCase("ROL USUARIO")) {
            txtcontrasena.setEnabled(false);
            txtcontrasena.setText("");
        } else {
            txtcontrasena.setEnabled(true);
        }

        // Evento al cambiar el rol
        cbrol.addItemListener(new java.awt.event.ItemListener() {
            public void itemStateChanged(java.awt.event.ItemEvent evt) {
                if (evt.getStateChange() == java.awt.event.ItemEvent.SELECTED) {
                    String rolSeleccionado = cbrol.getSelectedItem().toString();
                    if (rolSeleccionado.equalsIgnoreCase("ROL USUARIO")) {
                        txtcontrasena.setEnabled(false);
                        txtcontrasena.setText("");
                    } else {
                        txtcontrasena.setEnabled(true); // si el rol no es usuario activo la caja de la contrasena
                        txtcontrasena.requestFocus();
                    }
                }
            }
        });
        txtcontrasena.addKeyListener(new java.awt.event.KeyAdapter() {
            @Override
            public void keyPressed(java.awt.event.KeyEvent evt) {
                if (evt.getKeyCode() == java.awt.event.KeyEvent.VK_ENTER) {
                    btniniciarsesion.doClick();
                }
            }
        });
    }

